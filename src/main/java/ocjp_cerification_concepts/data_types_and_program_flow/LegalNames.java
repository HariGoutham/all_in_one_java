//Characters Allowed: Variable names can include letters (both uppercase and lowercase), digits (0-9), underscores (_), and dollar signs ($).
//                    They cannot start with a digit.

//Case Sensitivity: Variable names are case-sensitive. For example, myVariable, MyVariable, and MYVARIABLE are considered different variables.
//
//Length: There is no explicit limit on the length of variable names, but they should be kept reasonably short for readability.
//
// Keywords: Variable names cannot be the same as Java reserved keywords (e.g., int, class, public, void, etc.).
//
//Special Characters: Variable names cannot contain spaces or special characters (e.g., @, #, %, etc.) except for underscores and dollar signs.
//
//Examples of Valid Variable Names

//        myVariable
//        my_variable
//        myVariable1
//        myVariable$
//        a
//        count
//        totalAmount
//        MAX_VALUE

//Examples of Invalid Variable Names

//        1stVariable (cannot start with a digit)
//        my-variable (contains a hyphen)
//        my variable (contains a space)
//        class (reserved keyword)
//        total# (contains a special character)


public class LegalNames {

    int $wer_ksjhdfj_$; //Valid
   // int $wer_ksjhdfj_$@; //Invalid (You can check by enabling it)
}
